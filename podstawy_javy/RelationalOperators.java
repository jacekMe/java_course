package podstawy_javy;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 8;
        int b = 88;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }
}
