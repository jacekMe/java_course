package src;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 8;
        int b = 88;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
    }

    /*
    OPERATORY RELACYJNE (PORÓWNANIA)

        ==  -> porównanie
        !=  -> porównanie czy są różne od siebie
        >   -> większe od
        <   -> mniejsze od
        >=  -> większe bądź równe
        <=  -> mniejsze bądź równe

     */
}
