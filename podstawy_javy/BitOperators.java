package podstawy_javy;

public class BitOperators {
    public static void main(String[] args) {

        System.out.println(14&11);
        System.out.println(10|11);
        System.out.println(10^11);
        System.out.println(10>>1);
        System.out.println(10<<2);
        System.out.println(~10);
    }
}
